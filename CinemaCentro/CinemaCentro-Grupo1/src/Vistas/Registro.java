    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            
            
            //validamos  q no haya campos vacios
            
            
            if(jTextNombre.getText().isEmpty() ||jTextDni.getText().isEmpty() || 
                    jDateNacimiento.getDate()==null || 
                    jContrasenia.getPassword().length ==0 ||
                    jComboMedioDePago.getSelectedIndex()==0){
                
                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos");
                return;
            }
            
            
            Comprador comprador = new Comprador();
            comprador.setNombre(jTextNombre.getText());
            comprador.setDni(Integer.parseInt(jTextDni.getText()));
            
            
            LocalDate fechaNac = jDateNacimiento.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            comprador.setFechaNac(fechaNac);
            
            comprador.setPassword(new String (jContrasenia.getPassword()));
            comprador.setMedioDePago((String)jComboMedioDePago.getSelectedItem());
            
            CompradorData compraData = new CompradorData();
            compraData.guardarComprador(comprador);
            
            
             JOptionPane.showMessageDialog(this, "Comprador registrado Correctamente");
             
             
             limpiarCampos();
            
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "El dni debe ser un numero valido");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error al registrar al comprador");
            
            
        
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         if(jTextDni.getText().isEmpty()){
              JOptionPane.showMessageDialog(this, "Debe ingresar el DNI del usuario que desea buscar ");
              return;
          }
        try{
        
            
            
       int dni = Integer.parseInt(jTextDni.getText());
        
       Comprador c = compradorData.buscarComprador(dni);
       
       if(c != null){
           jTextNombre.setText(c.getNombre());
           jTextDni.setText(String.valueOf(c.getDni()));
           jDateNacimiento.setDate(java.sql.Date.valueOf(c.getFechaNac()));
           jContrasenia.setText(c.getPassword());
           jComboMedioDePago.setSelectedItem(c.getMedioDePago());
       }else{
           JOptionPane.showMessageDialog(this, "No se encontro comprador con ese dni");
       }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error al buscar comprador");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            
            String nombre = jTextNombre.getText();
            int dni = Integer.parseInt(jTextDni.getText());
            Date fecha = jDateNacimiento.getDate();
            LocalDate fechaNacimiento = fecha.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            String contrasena = new String (jContrasenia.getPassword());
            String medioPago = (String) jComboMedioDePago.getSelectedItem();
            
            Comprador c = compradorData.buscarComprador(dni);
            
                if(c == null){
                    JOptionPane.showMessageDialog(this, "No se encontro comprador con ese ID");
                }
                
                c.setDni(dni);
                c.setNombre(nombre);
                c.setFechaNac(fechaNacimiento);
                c.setPassword(contrasena);
                c.setMedioDePago(medioPago);
                
                compradorData.actualizarComprador(c);
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar comprador");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

