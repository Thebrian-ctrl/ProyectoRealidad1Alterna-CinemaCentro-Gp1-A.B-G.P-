    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:
        Date fechita = jDateChooser1.getDate();
        
        if(fechita == null){
            JOptionPane.showMessageDialog(this, "debe seleccionar una fecha");
            return;
        }
        
        LocalDate fecha = fechita.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        
        modelo.setRowCount(0);
        
        List<Comprador> compradores = compradorData.listarCompradorPorFecha(fecha);
        
        for (Comprador c : compradores) {
            modelo.addRow(new Object[]{c.getIdComprador(), c.getDni(), c.getNombre(), c.getPassword(), c.getMedioDePago(), c.getFechaNac()});
        }
        
        if(compradores.isEmpty()){
            JOptionPane.showMessageDialog(this, "No asistieron clientes en esa fecha");
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed


